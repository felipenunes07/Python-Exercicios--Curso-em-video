import math
a = float(input('Digite o 창ngulo que voce deseja: '))
print('O 창ngulo de {} tem o SENO de {:.2f}.'.format(a,math.sin(math.radians(a))))
print('O 창ngulo de {} tem o COSSENO de {:.2f}.'.format(a,math.cos(math.radians(a))))
print('O 창ngulo de {} tem a TANGENTE de {:.2f}.'.format(a,math.tan(math.radians(a))))